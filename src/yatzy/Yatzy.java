    //Metod för att räkna ut totalpoängen för högra sidan av scorecard,
    private int sumLeft(){
        int sum = 0;
        for (int i = 0; i < sumLeft.size(); i++) {
           sum += sumLeft.get(i);
        }
        return sum;
    }
    
    //Metod för att räkna ut totalpoängen för högra sidan av scorecard
    private int sumRight(){
        int sum = 0;
        for (int i = 0; i < sumRight.size(); i++) {
           sum += sumRight.get(i);
        }
        return sum;
    }
    
    //Metod för att se om spelaren har rätt till bonuspoäng
    private boolean Bonus(){
        //om summan av ettor-sexor 63p eller högre, ger ett bonuspoäng
        if (sumLeft() > 62) {
            return true;
        }
        return false;
    }
    
    //Metod för att sortera värden
    public void BubbelSortera(int[]lista){
        for (int m = lista.length-1; m > 0; m--) {
            for (int n = 0; n < m; n++) {
                if (lista[n]>lista[n+1]) {
                    int temp = lista[n];
                    lista[n] = lista[n+1];
                    lista[n+1] = temp;
                }
            }
        }
    }
    
