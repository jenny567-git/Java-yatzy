    /* Metod för att räkna högra sidan av scorecard exkl. liten stege, stor stege och chans.
    * Undersöker hur frekvent varje tärningsvärde är inom tärningskastet, vilka är de två mest 
    * frekventa tärningsvärdena.
    * Undersöker om spelaren är kvalificerad för poäng inom kategorin och räknar poängen.
    */
    public int qualifyRight(String name){
        int sum = 0;
        int[]frekvens = new int[6];        //sortera efter nummervärde
        for (int i = 0; i < 5; i++) {   //fem tärningar att count
            frekvens[dicePosition[i]-1]++;         //räknar and store frekvens av dicevärde i frekvens-array(tärningsfrekvens av 1-6)
        }
        
        int topIdx1 = 0, topIdx2 = 0;   //hitta de två mest frekventa tärningsvärde, top1 & top2
        
        for (int i = 1; i < 6; i++){
            if (frekvens[i] > frekvens[topIdx1]) {    //hitta tärningsindex som är mest frekvent med högst värde
                topIdx2 = topIdx1;  
                topIdx1 = i;
            } else if (frekvens[i] > frekvens[topIdx2]) { //hitta tärningsindex som är nästmest frekvent med nästhögst värde
                topIdx2 = i;
            }
        }
        
        
        if (name.equals("yatzy") && frekvens[topIdx1] == 5) {
            // Yatzy                poäng = 50;
            sum = YATZY;
        } else if (name.equals("fyrtal") && frekvens[topIdx1] >= 4) {
            // Fyrtal               poäng = 4*(topIdx1+1)
            sum = 4*(topIdx1+1);
        } else if (name.equals("triss") || name.equals("kak") && frekvens[topIdx1] == 3) {
            if (name.equals("kak") && frekvens[topIdx2] == 2) {
                // Kåk              poäng = 3*(topIdx1+1) + 2*(topIdx2+1)
                sum = (3*(topIdx1+1) + 2*(topIdx2+1));
            } else if (name.equals("triss")){
                // Triss            poäng = 3*(topIdx1+1)
                sum = 3*(topIdx1+1);
            }
        } else if (name.equals("tvaPar") && frekvens[topIdx1] >= 2) {
            if (frekvens[topIdx2] == 2) {
                // Två par          poäng = 2*(topIdx1+1) + 2*(topIdx2+1)
                sum = (2*(topIdx1+1) + 2*(topIdx2+1));
            } else if (frekvens[topIdx1] == 4){
                    sum = (4*(topIdx1+1));
            }
        } else if (name.equals("ettPar") && frekvens[topIdx1] >= 2){     //poäng vid två par fel, väljer lägsta paret
            // Ett par          poäng = 2*(topIdx1+1)
            sum = 2*(topIdx1+1);
        }
        return sum;
    }
    
