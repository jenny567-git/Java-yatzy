    private void FirstRoll(){
        for (int n = 0; n < ANTAL_DICE; n++) {
            Kasta();
            dicePosition[n] = value;     //tilldelar värdet
        }
        //visa diceValue på displayen(tärningsbutton)
        for (int m = 0; m < ANTAL_DICE; m++) {
            if (m == 0) {
                tglbtnDice1.setText("" + dicePosition[m]);
            }
            if (m == 1) {
                tglbtnDice2.setText("" + dicePosition[m]);
            }
            if (m == 2) {
                tglbtnDice3.setText("" + dicePosition[m]);
            }
            if (m == 3) {
                tglbtnDice4.setText("" + dicePosition[m]);
            }
            if (m == 4) {
                tglbtnDice5.setText("" + dicePosition[m]);
            }
            
            diceCount = 1;
        }
    }
    
    private void SecondAndThirdRoll(){
        //klickade tärningar kastas om
        if (tglbtnDice1.isSelected()) {
            Kasta();
            dicePosition[0] = value;
        }
        if (tglbtnDice2.isSelected()) {
            Kasta();
            dicePosition[1] = value;
        }
        if (tglbtnDice3.isSelected()) {
            Kasta();
            dicePosition[2] = value;
        }
        if (tglbtnDice4.isSelected()) {
            Kasta();
            dicePosition[3] = value;
        }
        if (tglbtnDice5.isSelected()) {
            Kasta();
            dicePosition[4] = value;
        }
        
        //visa på displayen
        for (int m = 0; m < ANTAL_DICE; m++) {
            if (m == 0) {
                tglbtnDice1.setText("" + dicePosition[m]);
            }
            if (m == 1) {
                tglbtnDice2.setText("" + dicePosition[m]);
            }
            if (m == 2) {
                tglbtnDice3.setText("" + dicePosition[m]);
            }
            if (m == 3) {
                tglbtnDice4.setText("" + dicePosition[m]);
            }
            if (m == 4) {
                tglbtnDice5.setText("" + dicePosition[m]);
            }
        }
        diceCount += 1;
    }
      
